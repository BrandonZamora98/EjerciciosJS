
// Sintaxis de switch
var dia = 4;
function diaSemana(){
    switch (dia) {
        case 0:
            return "Domingo";
        case 1:
            return "Lunes";
        case 2:
            return "Martes";
        case 3:
            return "Miercoles";
        case 4:
            return "Jueves";
        case 5:
            return "viernes"
        case 6:
            return "sabado";
        default:
            return "ingresa un dato valido";    
    }
}

diaSemana(dia);

